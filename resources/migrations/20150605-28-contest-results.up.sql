CREATE TABLE contest_results (id BIGINT NOT NULL,
                              results_id BIGINT REFERENCES results (id) NOT NULL,
                              PRIMARY KEY (results_id, id),
                              contest_id BIGINT,
                              jurisdiction_id BIGINT,
                              entire_district BOOLEAN NOT NULL,
                              total_votes INTEGER,
                              total_valid_votes INTEGER,
                              overvotes INTEGER,
                              blank_votes INTEGER,
                              accepted_provisional_votes INTEGER,
                              rejected_votes INTEGER,
                              certification TEXT);
